package main

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
	"sort"

	"github.com/kweezl/spacecraft-corporation/internal/gamedata/schema"
)

const (
	modulePath       = "github.com/kweezl/spacecraft-corporation"
	schemaImportPath = modulePath + "/internal/gamedata/schema"
	i18nImportPath   = modulePath + "/internal/i18n"
	dbImportBase     = modulePath + "/internal/gamedata/db/"
)

// goLit renders v as a Go composite literal. fmt's %#v prints named types as
// `schema.Item{...}` / `i18n.Language` and sorts map keys, so the output is
// valid, deterministic Go once the file imports the referenced packages.
func goLit(v any) string { return fmt.Sprintf("%#v", v) }

// writeGoFile formats and writes a generated source file with the given imports.
func writeGoFile(path, pkg, body string, imports ...string) error {
	var b bytes.Buffer
	b.WriteString("// Code generated by gamedata/gen. DO NOT EDIT.\n\n")
	fmt.Fprintf(&b, "package %s\n\n", pkg)
	if len(imports) > 0 {
		b.WriteString("import (\n")
		for _, imp := range imports {
			fmt.Fprintf(&b, "\t%q\n", imp)
		}
		b.WriteString(")\n\n")
	}
	b.WriteString(body)
	src, err := format.Source(b.Bytes())
	if err != nil {
		return fmt.Errorf("format %s: %w\n--- source ---\n%s", path, err, b.String())
	}
	return os.WriteFile(path, src, 0o644)
}

// emitVersion writes the generated Go for one version into dir (db/<version>).
// For a base version (parent == "") d holds the full data; for a delta layer it
// holds only the entries that differ from the parent, plus removedItems.
func emitVersion(dir, pkg, version, parent string, d dataset, removedItems []schema.GDID) error {
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return err
	}
	if removedItems == nil {
		removedItems = []schema.GDID{}
	}

	files := []struct {
		name    string
		body    string
		imports []string
	}{
		{"version_gen.go", fmt.Sprintf("const Version = %q\n\nconst Parent = %q\n", version, parent), nil},
		{"items_gen.go", fmt.Sprintf("var Items = %s\n\nvar RemovedItems = %s\n", goLit(d.Items), goLit(removedItems)), []string{schemaImportPath}},
		{"categories_gen.go", fmt.Sprintf("var Categories = %s\n", goLit(d.Categories)), []string{schemaImportPath}},
		{"contracts_gen.go", fmt.Sprintf("var Contracts = %s\n", goLit(d.Contracts)), []string{schemaImportPath}},
		{"space_objects_gen.go", fmt.Sprintf("var SpaceObjects = %s\n", goLit(d.SpaceObjects)), []string{schemaImportPath}},
		{"i18n_gen.go", fmt.Sprintf("var Names = %s\n\nvar Descs = %s\n\nvar CategoryNames = %s\n\nvar ContractNames = %s\n\nvar FactionNames = %s\n\nvar SpaceObjectNames = %s\n",
			goLit(d.Names), goLit(d.Descs), goLit(d.CategoryNames), goLit(d.ContractNames), goLit(d.FactionNames), goLit(d.SpaceObjNames)), []string{i18nImportPath, schemaImportPath}},
	}
	for _, f := range files {
		if err := writeGoFile(filepath.Join(dir, f.name), pkg, f.body, f.imports...); err != nil {
			return err
		}
	}
	return nil
}

// emitRegistry regenerates the gamedata-package map of every defined version to
// its package vars. It scans dbRoot for v<N> directories so adding a version is
// a regenerate, not a hand edit.
func emitRegistry(gamedataDir, dbRoot string) error {
	versions, err := definedVersions(dbRoot)
	if err != nil {
		return err
	}

	var b bytes.Buffer
	b.WriteString("// Code generated by gamedata/gen. DO NOT EDIT.\n\n")
	b.WriteString("package gamedata\n\n")
	b.WriteString("import (\n")
	for _, v := range versions {
		fmt.Fprintf(&b, "\t%s %q\n", v, dbImportBase+v)
	}
	b.WriteString(")\n\n")
	b.WriteString("var definedSources = map[string]source{\n")
	for _, v := range versions {
		fmt.Fprintf(&b, "\t%q: {\n", v)
		fmt.Fprintf(&b, "\t\tversion: %s.Version, parent: %s.Parent,\n", v, v)
		fmt.Fprintf(&b, "\t\titems: %s.Items, removedItems: %s.RemovedItems,\n", v, v)
		fmt.Fprintf(&b, "\t\tcategories: %s.Categories, contracts: %s.Contracts, spaceObjects: %s.SpaceObjects,\n", v, v, v)
		fmt.Fprintf(&b, "\t\tnames: %s.Names, descs: %s.Descs, categoryNames: %s.CategoryNames,\n", v, v, v)
		fmt.Fprintf(&b, "\t\tcontractNames: %s.ContractNames, factionNames: %s.FactionNames, spaceObjectNames: %s.SpaceObjectNames,\n", v, v, v)
		b.WriteString("\t},\n")
	}
	b.WriteString("}\n")

	src, err := format.Source(b.Bytes())
	if err != nil {
		return fmt.Errorf("format registry_gen.go: %w\n%s", err, b.String())
	}
	return os.WriteFile(filepath.Join(gamedataDir, "registry_gen.go"), src, 0o644)
}

// definedVersions returns the sorted v<N> package dirs under dbRoot.
func definedVersions(dbRoot string) ([]string, error) {
	entries, err := os.ReadDir(dbRoot)
	if err != nil {
		return nil, err
	}
	var out []string
	for _, e := range entries {
		if e.IsDir() && isVersionDir(e.Name()) {
			out = append(out, e.Name())
		}
	}
	sort.Slice(out, func(i, j int) bool { return versionNum(out[i]) < versionNum(out[j]) })
	return out, nil
}
